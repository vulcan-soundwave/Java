package entity;

public class Info {
	private int id;
	private String name;
	private String json;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}



	public String getJson() {
		return json;
	}

	public void setJson(String json) {
		this.json = json;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}



}
