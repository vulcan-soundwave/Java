package entity;

public class Heartrate {
private int eid;
private String time;
private int rate;
public int getEid() {
	return eid;
}
public void setEid(int eid) {
	this.eid = eid;
}
public String getTime() {
	return time;
}
public void setTime(String time) {
	this.time = time;
}
public int getRate() {
	return rate;
}
public void setRate(int rate) {
	this.rate = rate;
}

}
